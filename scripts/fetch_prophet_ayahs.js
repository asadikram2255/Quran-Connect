// ---------- Main ----------

async function main() {
  const results = {};

  for (const p of PROPHETS) {
    console.log(`\nFetching: ${p.name} (${p.searchTerms.join(", ")})`);
    let apiAyahs = [];
    for (const term of p.searchTerms) {
      try {
        const found = await searchArabic(term);
        console.log(`  API "${term}": ${found.length} matches`);
        apiAyahs = apiAyahs.concat(found);
        await sleep(400);
      } catch(e) {
        console.log(`  API error for "${term}":`, e.message);
      }
    }
    const combined = unique([...apiAyahs, ...p.extra]);
    results[p.key] = { ...p, apiAyahs, combinedAyahs: combined };
    console.log(`  Total combined: ${combined.length} ayahs`);
  }

  // Generate the JS block
  let js = "const PROPHET_DATA = [\n";
  for (const p of PROPHETS) {
    const r = results[p.key];
    const ayahsJson = JSON.stringify(r.combinedAyahs);
    const tvKey = p.topicVersesKey
      ? `...((()=>{try{return TOPIC_VERSES[${JSON.stringify(p.topicVersesKey)}]||[]}catch(e){return[]}})()),`
      : "";
    js += `  {\n`;
    js += `    key: ${JSON.stringify(p.key)}, name: ${JSON.stringify(p.name)}, arabic: ${JSON.stringify(p.arabic)}, surah: ${JSON.stringify(p.surah)},\n`;
    if (tvKey) {
      js += `    ayahs: [...new Set([${tvKey}...${ayahsJson}])]\n`;
    } else {
      js += `    ayahs: ${ayahsJson}\n`;
    }
    js += `  },\n`;
  }
  js += "];\n";

  // Write the result file
  const outPath = path.join(__dirname, "prophet_ayahs_result.js");
  fs.writeFileSync(outPath, js, "utf8");
  console.log(`\n\nWrote ${outPath}`);

  // Also print counts
  console.log("\n=== Summary ===");
  for (const p of PROPHETS) {
    const r = results[p.key];
    console.log(`${p.name.padEnd(30)} ${String(r.combinedAyahs.length).padStart(4)} ayahs`);
  }
}

main().catch(console.error);
