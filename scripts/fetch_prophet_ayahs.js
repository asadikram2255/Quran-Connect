/**
 * fetch_prophet_ayahs.js
 *
 * Queries the alquran.cloud API to find every ayah where each of the
 * 25 Quranic prophets is explicitly mentioned by name in the Arabic text.
 * Merges those with the existing myislam.org TOPIC_VERSES entries and any
 * curated extra ayahs (e.g. story passages, passages where the prophet is
 * addressed but not named).
 *
 * Run with:  node scripts/fetch_prophet_ayahs.js
 */

const https = require("https");
const fs    = require("fs");
const path  = require("path");

// ---------- helpers ----------

function sleep(ms) { return new Promise(r => setTimeout(r, ms)); }

function get(url) {
  return new Promise((resolve, reject) => {
    https.get(url, { headers: { "User-Agent": "Mozilla/5.0" } }, res => {
      let d = "";
      res.on("data", c => d += c);
      res.on("end", () => {
        try { resolve(JSON.parse(d)); }
        catch(e) { reject(new Error("JSON parse fail for " + url + ": " + d.slice(0,200))); }
      });
    }).on("error", reject);
  });
}

async function searchArabic(term) {
  const url = `https://api.alquran.cloud/v1/search/${encodeURIComponent(term)}/all/ar`;
  const data = await get(url);
  if (data.status !== "OK" || !data.data) return [];
  return (data.data.matches || []).map(m => {
    const [s, v] = (m.surah.number + ":" + m.numberInSurah).split(":");
    return `${m.surah.number}:${m.numberInSurah}`;
  });
}

function unique(arr) {
  return [...new Set(arr)].sort((a, b) => {
    const [as, av] = a.split(":").map(Number);
    const [bs, bv] = b.split(":").map(Number);
    return as !== bs ? as - bs : av - bv;
  });
}

