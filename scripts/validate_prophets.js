/**
 * validate_prophets.js
 * Cross-checks every ayah ID in every prophet's list against actual Quran data files.
 * Reports missing IDs and the highest surah covered per prophet.
 */

const fs   = require("fs");
const base = "D:/Users/asadi/Documents/GitHub/quran-better-for-me/data/";
const map  = JSON.parse(fs.readFileSync(base + "meta/shard_map_quran.json", "utf8"));

// Build set of all valid ayah IDs from the data
const allAyahs = new Set();
for (const [, p] of Object.entries(map)) {
  const shard = JSON.parse(fs.readFileSync(base + p, "utf8"));
  for (const rec of shard) allAyahs.add(rec.ayah_id);
}
console.log("Total valid ayah IDs in data:", allAyahs.size, "\n");

// ── Helper fns (must match update_prophet_data.js) ──────────────────────────
function u(arr) {
  return [...new Set(arr)].sort((a, b) => {
    const [as2, av] = a.split(":").map(Number);
    const [bs2, bv] = b.split(":").map(Number);
    return as2 !== bs2 ? as2 - bs2 : av - bv;
  });
}
function sr(s, from, to) {
  return Array.from({length: to - from + 1}, (_, i) => `${s}:${i + from}`);
}

