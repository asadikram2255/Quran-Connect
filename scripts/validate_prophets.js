// ── Validate ─────────────────────────────────────────────────────────────────
console.log("=== AYAH VALIDATION ===\n");
let totalMissing = 0;
for (const p of PROPHETS) {
  const missing = p.ayahs.filter(id => !allAyahs.has(id));
  const surahs  = [...new Set(p.ayahs.map(id => id.split(":")[0]).map(Number))].sort((a,b)=>a-b);
  const maxSurah = surahs[surahs.length - 1];

  if (missing.length > 0) {
    console.log(`${p.name} — ${p.ayahs.length} ayahs, max surah=${maxSurah}, MISSING ${missing.length}:`);
    console.log(`  ${missing.join(", ")}`);
    totalMissing += missing.length;
  } else {
    console.log(`${p.name.padEnd(22)} ${String(p.ayahs.length).padStart(4)} ayahs  surahs ${surahs[0]}–${maxSurah}  ✓`);
  }
}
console.log(`\nTotal invalid IDs: ${totalMissing}`);
